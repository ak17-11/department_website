                framework of Computer Science was introduced in year 2004.
                Currently the department is running Master of Computer
                Applications (MCA) (2 years full time course). Master of
                Computer Applications (MCA) (2 years full time course) in Self
                Financed mode and Master of Science (Computer Science) under the
                framework of the Hon's School (02 years full time course).
                Admissions to these course are held through an annual entrance
                test conducted on All India basis by Panjab University. In
                addition, Ph.D. Program has been started from year 2005.
